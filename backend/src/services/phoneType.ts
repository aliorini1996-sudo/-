/**
 * تمييز الجوّال من الأرضي — لاستهداف واتساب.
 *
 * لماذا: مصيدة الليدز تجلب أرقام الشركات كما هي، وأغلبها **أرضي**. قياس على الإنتاج:
 * 145 من 183 محاولة إرسال كانت لأرقام لا واتساب لها. أثر ذلك مضاعف:
 *   - تُهدر الحصّة اليومية على أرقام لن تصلها رسالة أبداً،
 *   - وكل محاولة فاشلة تُضعف سمعة رقمك أمام واتساب.
 *
 * لا نحذف الأرضي من قاعدة العملاء — هو عميل صالح يُتصل به هاتفياً ويُراسَل بريدياً؛
 * نستبعده من استهداف واتساب وحده.
 *
 * المرجع: بادئات الجوّال الوطنية لكل دولة (ITU + هيئات الاتصالات المحلية).
 */

// بادئات الجوّال بعد رمز الدولة، لكل رمز دولي
const MOBILE_PREFIXES: Record<string, string[]> = {
  '966': ['5'],                    // السعودية
  '971': ['5'],                    // الإمارات (الأرضي 2/3/4/6/7/9)
  '20': ['1'],                     // مصر (10/11/12/15)
  '965': ['5', '6', '9'],          // الكويت
  '974': ['3', '5', '6', '7'],     // قطر
  '973': ['3'],                    // البحرين
  '968': ['7', '9'],               // عُمان
  '962': ['7'],                    // الأردن
  '964': ['7'],                    // العراق
  '961': ['3', '7', '8'],          // لبنان (70/71/76/78/79/81)
  '963': ['9'],                    // سوريا
  '967': ['7'],                    // اليمن
  '970': ['5'],                    // فلسطين
  '972': ['5'],                    // فلسطين (رمز بديل)
  '218': ['9'],                    // ليبيا
  '249': ['9', '1'],               // السودان
  '216': ['2', '4', '5', '9'],     // تونس (الأرضي 7)
  '213': ['5', '6', '7'],          // الجزائر (الأرضي 2/3/4)
  '212': ['6', '7'],               // المغرب (الأرضي 5)
  '222': ['2', '3', '4'],          // موريتانيا
};

// الرموز الدولية مرتّبة تنازلياً بالطول: 971 قبل 97 قبل 9 — وإلا التبست الدول
const CODES = Object.keys(MOBILE_PREFIXES).sort((a, b) => b.length - a.length);

export type PhoneKind = 'mobile' | 'landline' | 'unknown';

/**
 * تصنيف رقم بصيغته الدولية (أرقام فقط، بلا + أو 00).
 * 'unknown' لدولة غير مدرجة أو رقم قصير — ولا نُقصيه: قد يكون جوّالاً، والتحقّق
 * النهائي يبقى عند الإرسال (isRegisteredUser). نُقصي المؤكَّد أرضياً فقط.
 */
export function classifyPhone(phone?: string | null): PhoneKind {
  if (!phone) return 'unknown';
  let d = String(phone).replace(/[^\d]/g, '');
  if (d.startsWith('00')) d = d.slice(2);
  if (d.length < 8) return 'unknown';

  const code = CODES.find((c) => d.startsWith(c));
  if (!code) return 'unknown';

  const rest = d.slice(code.length).replace(/^0+/, ''); // 9660555… ⇒ 555…
  if (!rest) return 'unknown';

  const prefixes = MOBILE_PREFIXES[code];
  return prefixes.some((p) => rest.startsWith(p)) ? 'mobile' : 'landline';
}

// true = جوّال · false = أرضي مؤكَّد · null = غير معروف (لا يُقصى)
export function phoneIsMobile(phone?: string | null): boolean | null {
  const k = classifyPhone(phone);
  return k === 'unknown' ? null : k === 'mobile';
}
